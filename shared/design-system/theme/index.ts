export type SymbiTheme = typeof symbiTheme;

// CSS custom properties generator
export function generateCssVariables(theme: typeof symbiTheme): string {
  const cssVars: string[] = [':root {'];
  
  // Colors
  Object.entries(theme.colors).forEach(([colorName, colorValues]) => {
    if (typeof colorValues === 'object') {
      Object.entries(colorValues).forEach(([shade, value]) => {
        cssVars.push(`  --symbi-${colorName}-${shade}: ${value};`);
      });
    }
  });
  
  // Typography
  Object.entries(theme.typography.fontSize).forEach(([size, value]) => {
    cssVars.push(`  --symbi-text-${size}: ${value};`);
  });
  
  // Spacing
  Object.entries(theme.spacing).forEach(([size, value]) => {
    cssVars.push(`  --symbi-spacing-${size}: ${value};`);
  });
  
  cssVars.push('}');
  
  return cssVars.join('\n');
}